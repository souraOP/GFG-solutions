class Solution {
  public:
    int totalTime(int n, vector<int> &arr, vector<int> &time) {
        vector<long long> a(n,-1);
        long long curr=0;
        for(int i=0;i<n;i++)
        {
            if(a[arr[i]-1]==-1)
                a[arr[i]-1]=curr;
            else
            {
                if(curr<a[arr[i]-1]+time[arr[i]-1])
                    curr=a[arr[i]-1]+time[arr[i]-1];
                a[arr[i]-1]=curr;
            }
            curr++;
        }
        return curr-1;
        // code here
    }
};


